#include "libft.h"

int		ft_get_number(char **s)
{
	unsigned int	res;
	int				sign;

	res = 0;
	sign = 1;
	while (**s && **s == ' ')
		(*s)++;
	if (**s == '-')
		sign = -1;
	if (**s == '-' || **s == '+')
		(*s)++;
	while (**s && ft_isdigit(**s))
	{
		res *= 10;
		res += **s - 48;
		(*s)++;
	}
	return (res * sign);
}
